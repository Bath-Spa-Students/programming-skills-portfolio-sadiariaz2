# Define a function called make_shirt with two parameters: size and message
def make_shirt(size, message):
    """Prints a sentence summarizing the size of the shirt and the message printed on it."""
    # Print a sentence summarizing the size of the shirt and the message
    print(f"A {size}-sized shirt will be made with the message: '{message}'.")

# Call the make_shirt function with size="medium" and message="Keep calm and code on"
make_shirt("medium", "Keep calm and code on")

# Call the make_shirt function with size="large" and message="I love Turkey"
make_shirt(size="large", message="I love Turkey")
